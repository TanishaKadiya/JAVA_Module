public class person {
    private String name;
    private int age;
    private String country;
    public String getname()
    {
        return name;
    }
    public void setname(String name)
    {
        this.name=name;
    }
    public int getage()
    {
        return age;
    }
    public void setage(int age)
    {
        this.age=age;
    }
    public String getcountry()
    {
        return country;
    }
    public void setcountry(String country)
    {
        this.country=country;
    }
    
    
}
